void printStack(stack<int> st) {
    while(!st.empty()) {
        cout << st.top() << " ";
        st.pop();
    }
    cout << endl;
}

void reverseStack(stack<int> & st) {
    if(st.empty()){
        return; 
    }
    stack<int> temp;
    int top = st.top();
    st.pop();

    //lets reverse the rest of the stack
    reverseStack(st);

    //lets push thise elements into another stack
    while(!st.empty()) {
        temp.push(st.top());
        st.pop();
    }
    //now push the initially recovered top element
    st.push(top);

    //now again fill this stack st with the elements put into the temp stack
    while(!temp.empty()) {
        st.push(temp.top());
        temp.pop();
    }
    return;
}

